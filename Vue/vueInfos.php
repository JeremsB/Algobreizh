<h2>Le lavage à l'eau de mer</h2>
<p>Aussitôt après la récolte les algues sont lavées à l'eau de mer pour retirer le sable, les
coquillages et les cailloux. L'utilisation de l'eau de mer est déterminante pour conserver la
texture et la couleur d'origine de l'algue.</p>
</div>
<div class="affichage">
<h2>Conservation</h2>
<p>Deux procédés sont utilisés aussitôt après le lavage : le salage ou la déshydratation.
Le salage se fait progressivement et jusqu'à saturation. Les algues sont ensuite stockées au froid
positif. Nous utilisons un sel sans anti-mottant et utilisable en Agriculture Biologique.
Ce procédé permet de conserver les algues plusieurs mois et nous garantissons une DLUO (date
limite d'utilisation optimale) de 4 mois.
Le salage affine les qualités gustatives des algues et leur confère une texture plus tendre.
La déshydratation se fait dans des séchoirs où la température est voisine de 40°C. Cette
déshydratation douce respecte au mieux le produit et notamment ses saveurs. Les algues
peuvent être ensuite broyées et tamisées pour être proposées en paillettes.</p>
</div>
<div class="affichage">
<h2>Les algues sauvages bretonnes</h2>
<br />
<h5> - LAITUE DE MER - </h5>
<p>La laitue de mer est une algue verte, souple, aux saveurs corsées auxquelles
se mêle un goût proche de celui de l'oseille. Elle apporte toute sa force et sa
fraîcheur à vos préparations.
Crue et émincée elle accompagnera les salades et les crudités. Hachée elle
agrémentera les sauces, les potages ou les vinaigrettes. Sa forme de feuille
permet aussi de l'utiliser pour réaliser une papillote.</p>

<h5> - NORI - </h5>
<p>Cette algue aux reflets violets devient presque noire lorsqu'elle est séchée. Ses
saveurs rappellent celles du thé fumé ou du champignon séché. La Nori est très
fine et souple.
Grillée sur la flamme puis émiettée, elle se met en caviar sur une pomme de
terre au four. Accompagne délicieusement les omelettes, les béchamels et les
sauces. C'est aussi l'algue impériale du Japon.
Vous trouverez aussi dans la cuisine Japonaise la feuille de Nori allant du noir au vert pour la
réalisation des sushis.</p>

<h5> - DULSE - </h5>
<p>La dulse est une algue rouge où se mélangent douceur et iode. On lui trouve
même un goût de noisette. Croquante au sortir de la mer, elle devient vite
fondante après une légère cuisson.
Elle se consomme crue avec les salades et les crudités. Agrémente les sauces,
les potages et les vinaigrettes.Elle enrobe un fromage de chèvre avant une légère cuisson vapeur et parfumera délicieusement
une tourte aux fruits de mer.<p>

<h5> - SPAGHETTI DE MER - </h5>
<p>De forme très élancée le spaghetti de mer est une algue brune tendre et iodée.
Il accompagne les poissons, les légumes ou les tourtes de légumes et de fruits
de mer.</p>

<h5> - KOMBU ROYAL - </h5>
<p>Cette laminaire est une des algues les plus majestueuses de l'océan. Les muses
l'ont appelée Baudrier de Neptune.
Charnue et croquante elle a le charme de marier des saveurs sucrées et iodées.
C'est la plus importante source d'iode connue. Par sa forme élancée et par la
largeur de sa lame, cette algue s'emploie généralement pour réalisation de
papillotes "royales".
Grâce à ses propriétés le kombu royal s'emploie également pour la cuisson des
légumineuses.</p>
